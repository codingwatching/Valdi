#!/usr/bin/env bash
#
# validate_external_config.sh — Verify that the open-source repo configuration
# remains valid after Copybara transforms are applied.
#
# This script simulates the key Copybara transforms (see copy.bara.sky) and
# validates the resulting configuration would work on the public GitHub CI.
# It runs inside the internal monorepo so regressions are caught BEFORE
# Copybara mirrors the change.
#
# Called by: client_validate_valdi_open_source.sh (SnapCI)

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
OPEN_SOURCE_DIR="$(cd "$SCRIPT_DIR/../.." && pwd)"

FAILURES=0
fail() {
    echo "FAIL: $1"
    FAILURES=$((FAILURES + 1))
}
pass() { echo "PASS: $1"; }

echo "=== Validating External Build Configuration ==="

# ---------------------------------------------------------------------------
# 1. NPM lockfile compatibility
#
# The publish-npm.yml workflow upgrades to npm@latest (v11+) which requires
# lockfileVersion >= 3. Lockfiles generated by npm <= 8 use v2 and fail
# with "Missing: @esbuild/... from lock file" under npm 11's stricter
# lockfile parsing.
# ---------------------------------------------------------------------------
echo ""
echo "--- NPM lockfile compatibility ---"

for lockfile in "$OPEN_SOURCE_DIR"/npm_modules/*/package-lock.json; do
    pkg_name="$(basename "$(dirname "$lockfile")")"
    version=$(grep -m1 '"lockfileVersion"' "$lockfile" | grep -Eo '[0-9]+')
    if [[ "$version" -lt 3 ]]; then
        fail "$pkg_name/package-lock.json has lockfileVersion $version (need >= 3 for npm 11+)"
    else
        pass "$pkg_name/package-lock.json lockfileVersion $version"
    fi
done

# ---------------------------------------------------------------------------
# 2. Copybara transform markers
#
# Verify the sentinel comments that Copybara's core.replace() keys on still
# exist. If someone removes the "# use_local_compiler" comment or changes
# "INTERNAL_BUILD = True", the outbound transform silently becomes a no-op
# and the public repo gets the wrong flag value.
# ---------------------------------------------------------------------------
echo ""
echo "--- Copybara transform markers ---"

if grep -q 'build_setting_default = False,  # use_local_compiler' "$OPEN_SOURCE_DIR/bzl/valdi/BUILD.bazel"; then
    pass "use_local_compiler marker present (internal=False, copybara flips to True)"
else
    fail "use_local_compiler marker missing or malformed in bzl/valdi/BUILD.bazel — copybara transform will silently fail"
fi

if grep -q 'INTERNAL_BUILD = True' "$OPEN_SOURCE_DIR/bzl/prebuilt_tools.bzl"; then
    pass "INTERNAL_BUILD marker present (internal=True, copybara flips to False)"
else
    fail "INTERNAL_BUILD marker missing or malformed in bzl/prebuilt_tools.bzl — copybara transform will silently fail"
fi

if [[ -f "$OPEN_SOURCE_DIR/bzl/additional_dependencies.bzl.copybara" ]]; then
    pass "additional_dependencies.bzl.copybara exists"
else
    fail "additional_dependencies.bzl.copybara missing — copybara will fail to overwrite external bzl/additional_dependencies.bzl"
fi

# ---------------------------------------------------------------------------
# 3. use_local_compiler=true build graph validity
#
# On the public repo, Copybara sets use_local_compiler=True. Verify that the
# build graph is valid under that flag on the current platform. This catches
# issues like missing Swift toolchains on Linux before they reach the public CI.
# ---------------------------------------------------------------------------
echo ""
echo "--- Build graph validity with use_local_compiler=true ---"

pushd "$OPEN_SOURCE_DIR" > /dev/null

# Pure C++ targets should always be analyzable, even with use_local_compiler=true.
# If this fails, it means the valdi_toolchain registration is broken (e.g., Swift
# toolchain missing on Linux).
CPP_TARGETS="//valdi:test_snap_drawing //valdi:test_hermes"
if bzl cquery "set($CPP_TARGETS)" --//bzl/valdi:use_local_compiler=true --output=label 2>/dev/null; then
    pass "C++ test targets analyzable with use_local_compiler=true"
else
    fail "C++ test targets CANNOT be analyzed with use_local_compiler=true — the registered valdi_toolchain likely can't resolve (missing Swift toolchain on Linux?)"
fi

# The compiler itself should be analyzable. On macOS this means rules_swift
# finds Xcode; on Linux it means the Swift toolchain is properly configured.
if bzl cquery //compiler/compiler:local_valdi_compiler --//bzl/valdi:use_local_compiler=true --output=label 2>/dev/null; then
    pass "local_valdi_compiler target analyzable"
else
    fail "local_valdi_compiler target CANNOT be analyzed — Swift toolchain likely missing"
fi

# Targets that use valdi_module() need the compiler. Verify the full chain works.
if bzl cquery //valdi:test_layout --//bzl/valdi:use_local_compiler=true --output=label 2>/dev/null; then
    pass "test_layout (uses compiler) analyzable with use_local_compiler=true"
else
    fail "test_layout CANNOT be analyzed with use_local_compiler=true — compiler toolchain chain is broken"
fi

popd > /dev/null

# ---------------------------------------------------------------------------
# 4. No stale git-lfs references
#
# After the LFS removal, no files should reference git-lfs (except the
# mirroring scripts which are excluded from the public repo).
# ---------------------------------------------------------------------------
echo ""
echo "--- No stale git-lfs references ---"

# Search for git-lfs references excluding mirroring scripts and this test
LFS_HITS=$(grep -rn "git.lfs\|git-lfs\|lfs: true\|setupGitLfs\|checkGitLfsInit" \
    "$OPEN_SOURCE_DIR" \
    --include="*.sh" --include="*.ts" --include="*.yml" --include="*.yaml" --include="*.md" \
    --exclude-dir=mirroring \
    --exclude="validate_external_config.sh" \
    2>/dev/null || true)

if [[ -z "$LFS_HITS" ]]; then
    pass "No stale git-lfs references"
else
    fail "Stale git-lfs references found:"
    echo "$LFS_HITS"
fi

# ---------------------------------------------------------------------------
# Summary
# ---------------------------------------------------------------------------
echo ""
echo "=== External config validation complete ==="
if [[ "$FAILURES" -gt 0 ]]; then
    echo "RESULT: $FAILURES check(s) failed"
    exit 1
else
    echo "RESULT: All checks passed"
fi
